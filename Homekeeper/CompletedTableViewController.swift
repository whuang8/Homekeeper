    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cellIdentifier = "CompletedTableViewCell"
        let cell = tableView.dequeueReusableCellWithIdentifier(cellIdentifier, forIndexPath: indexPath) as! CompletedTableViewCell
        
        let todo = items[indexPath.row]
        
        cell.task.text = todo.task
        cell.message.text = todo.message
        cell.user.text = todo.checkedUser
        
        return cell
    }
    
    
